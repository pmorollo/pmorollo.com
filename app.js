function handleSearch(query) {
    if (!query.trim()) { renderArticles(currentFilter); return; }
    const grid = document.getElementById('articles-grid');
    const results = articles.filter(a =>
        a.title.toLowerCase().includes(query.toLowerCase()) ||
        a.excerpt.toLowerCase().includes(query.toLowerCase()) ||
        a.tags.some(t => t.toLowerCase().includes(query.toLowerCase()))
    );
    if (results.length === 0) {
        grid.innerHTML = `<div class="empty-state"><p>Nenhum resultado para "${query}".</p></div>`;
        return;
    }
    grid.innerHTML = results.map(article => `
        <article class="article-card" onclick="openArticle(${article.id})">
            <div class="article-card-image">
                <img src="${article.thumb}" alt="${article.title}" loading="lazy">
                <span class="article-card-category">${article.category}</span>
            </div>
            <div class="article-card-body">
                <h3 class="article-card-title">${article.title}</h3>
                <p class="article-card-excerpt">${article.excerpt}</p>
                <div class="article-card-footer">
                    <div class="article-card-meta">
                        <img class="author-avatar-img" src="${article.authorImg}" alt="${article.author}">
                        <div>
                            <span class="author-name">${article.author}</span>
                            <span class="meta-details">${article.date} · ${article.readTime}</span>
                        </div>
                    </div>
                </div>
            </div>
        </article>
    `).join('');
}

function handleSubscribe(e) {
    e.preventDefault();
    const input = e.target.querySelector('input');
    const email = input.value.trim();
    if (!email || !email.includes('@')) {
        showToast('Por favor, insira um email válido.');
        return;
    }
    const subscribers = JSON.parse(localStorage.getItem('newsletter_subscribers') || '[]');
    if (subscribers.find(s => s.email === email)) {
        showToast('Este email já está inscrito!');
        input.value = '';
        return;
    }
    subscribers.push({ email: email, date: new Date().toISOString() });
    localStorage.setItem('newsletter_subscribers', JSON.stringify(subscribers));
    input.value = '';
    showToast('Inscrição realizada com sucesso! Bem-vindo(a) ao Clube da Leitura.');
}

function getSubscribers() {
    return JSON.parse(localStorage.getItem('newsletter_subscribers') || '[]');
}

function exportSubscribers() {
    const subs = getSubscribers();
    if (subs.length === 0) {
        showToast('Nenhum inscrito encontrado.');
        return;
    }
    let csv = 'Email,Data de Inscrição\n';
    subs.forEach(s => {
        csv += s.email + ',' + new Date(s.date).toLocaleDateString('pt-BR') + '\n';
    });
    const blob = new Blob([csv], { type: 'text/csv' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'inscricoes_pagina_virada.csv';
    a.click();
    URL.revokeObjectURL(url);
    showToast(subs.length + ' inscrito(s) exportado(s)!');
}

function updateShareLinks(article) {
    const url = encodeURIComponent(window.location.href);
    const text = encodeURIComponent(article.title + ' - Pagina Virada');
    document.getElementById('share-twitter').href = `https://twitter.com/intent/tweet?url=${url}&text=${text}`;
    document.getElementById('share-facebook').href = `https://www.facebook.com/sharer/sharer.php?u=${url}`;
    document.getElementById('share-whatsapp').href = `https://wa.me/?text=${text}%20${url}`;
}

